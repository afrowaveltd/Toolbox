namespace Afrowave.Toolbox.Essentials.Enums;

/// <summary>
/// Describes the direction used for sorting values.
/// </summary>
public enum SortDirection
{
   /// <summary>
   /// No sort direction was specified.
   /// </summary>
   None = 0,

   /// <summary>
   /// Sort values in ascending order.
   /// </summary>
   Ascending = 1,

   /// <summary>
   /// Sort values in descending order.
   /// </summary>
   Descending = 2
}